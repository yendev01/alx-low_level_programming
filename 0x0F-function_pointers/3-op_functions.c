#include <stdlib.h>

/**
  * op_add - adds
  * @a: ..
  * @b: ..
  * Return: returns int
  */

int op_add(int a, int b)
{
	return (a + b);
}


/**
  * op_sub - substracts
  * @a: ..
  * @b: ..
  * Return: returns int
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - multiplies
  * @a: ..
  * @b: ..
  * Return: returns int
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - divides
  * @a: ..
  * @b: ..
  * Return: returns int
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - modulus
  * @a: ..
  * @b: ..
  * Return: returns int
  */

int op_mod(int a, int b)
{
	return (a % b);
}
