#include <stdio.h>

/**
  * rev_string - entry point
  * description: function that reverses a string
  * Return: returns nothing
  * @s: input
  */

void rev_string(char *s)
{

	*(s + 0) = 'l';
	*(s + 1) = 'o';
	*(s + 2) = 'o';
	*(s + 3) = 'h';
	*(s + 4) = 'c';
	*(s + 5) = 'S';
	*(s + 6) = ' ';
	*(s + 7) = 'y';
	*(s + 8) = 'M';

}
